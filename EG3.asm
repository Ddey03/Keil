	;ALP to transfer a block of 10 data bytes from source to destination
	AREA PROG_3,CODE,READONLY
	ENTRY
START
	LDR R1,=SRC
	LDR R2,=DST
	MOV R3,#0X0A
LOOP
	LDRB R4,[R1],#1
	STRB R4,[R2],#1
	SUBS R3,R3,#1
	BNE LOOP
HERE B HERE
SRC DCB 0X11,0X22,0X33,0X44,0X55,0X66,0X77,0X88,0X99,0XAA
	AREA DSTB,DATA,READWRITE
DST SPACE 10
	END